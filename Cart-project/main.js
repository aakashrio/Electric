function explore() {
  // for loop creating card how many elements present in data array
  let at = 0;
  let t = 0;
  for (i = 0; i < games.length; i++) {
    //creating display card for games
    const a = document.createElement("div");
    document.getElementById("main").append(a);
    a.className = "contentdisplay";
    a.id = "g" + at;

    //creating for insert images on card
    const b = document.createElement("img");
    a.append(b);
    b.className = "displayimage";
    b.src = games[i].image;

    //creating for displying names

    const c1 = document.createElement("span");
    a.append(c1);
    c1.className = "displaybase";
    c1.innerHTML = "Base Game";

    const c = document.createElement("span");
    a.append(c);
    c.className = "displayname";
    c.innerHTML = games[i].name;

    //creating for display prices
    const d = document.createElement("span");
    a.append(d);
    d.className = "displayprice";
    d.innerHTML =
      games[i].price +
      " " +
      '<i class="fa-solid fa-indian-rupee-sign fa-sm"></i>';

    //creating cart button container

    const e = document.createElement("div");
    a.append(e);
    e.className = "buttoncontainer";
    e.id = "b" + t;
    t = t + 1;

    //creating cart button

    var cartadd = function () {
      let h2 = this.parentElement.id;
      let res = h2.slice(1);
      alert(games[res].name + " " + "was added in your Cart");
      let cart = new Set(JSON.parse(localStorage.getItem("cart")) || []);
      cart.add(games[res]);
      localStorage.setItem("cart", JSON.stringify(Array.from(cart)));
    };

    const f = document.createElement("button");
    e.append(f);
    f.className = "cartbutton";
    f.innerHTML = '<i class="fa-solid fa-cart-shopping">';
    f.addEventListener("click", cartadd);

    var whiashdd = function () {
      let h2 = this.parentElement.id;
      let res = h2.slice(1);
      alert(games[res].name + " " + "was added in your Whishlist");
      let whlist = new Set(JSON.parse(localStorage.getItem("whlist")) || []);
      whlist.add(games[res]);
      localStorage.setItem("whlist", JSON.stringify(Array.from(whlist)));
    };

    //creating whishlist buttton

    const g = document.createElement("button");
    e.append(g);
    g.className = "whishbutton";
    g.innerHTML = '<i class="fa-regular fa-bookmark">';
    g.addEventListener("click", whiashdd);
    at = at + 1;
  }
}

